package com.loops;
import java.util.Scanner;

public class PrintEvenNo {

	public static void main(String[] args) {
		int number=229;  
		System.out.print("List of even numbers from 1 to "+number+": ");  
		for (int i=121; i<=number; i++)   
		{  
		
		if (i%2!=0)   
		{  
		System.out.print(i + " ");  
		}  
		}  
		}  
		}  